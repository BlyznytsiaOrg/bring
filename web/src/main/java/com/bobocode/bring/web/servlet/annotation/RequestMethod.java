package com.bobocode.bring.web.servlet.annotation;

/**
 * Enumeration representing the standard HTTP methods. These methods define the
 * actions that can be performed on a resource identified by a URL.
 *
 * @author Blyzhnytsia Team
 * @since 1.0
 */
public enum RequestMethod {
    GET, POST, PUT, PATCH, DELETE, HEAD, OPTIONS
}
